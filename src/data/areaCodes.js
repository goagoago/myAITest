/**
 * 获取省份列表
 */
export function getProvinces() {
  return Object.entries(areaData).map(([code, item]) => ({
    code,
    name: item.name,
  }))
}

/**
 * 获取指定省份的城市列表
 */
export function getCities(provinceCode) {
  const province = areaData[provinceCode]
  if (!province) return []
  return Object.entries(province.children).map(([code, item]) => ({
    code,
    name: item.name,
  }))
}

/**
 * 获取指定城市的区县列表
 */
export function getDistricts(provinceCode, cityCode) {
  const city = areaData[provinceCode]?.children[cityCode]
  if (!city) return []
  return Object.entries(city.children).map(([code, name]) => ({
    code,
    name,
  }))
}
